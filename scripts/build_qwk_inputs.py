"""Build QWK-ready per-task-model datasets from outputs/test_eval/predictions/.

For each row in the test-eval predictions, decompose `gold_score` and
`pred_score` (1-15) into two ordinal components:

    score 1..5    -> direction = -1, confidence = score
    score 6..10   -> direction =  0, confidence = score - 5
    score 11..15  -> direction = +1, confidence = score - 10

Group by `task_model_slug`, write one JSONL + one slim CSV per task model
to `data/qwk/`, plus a manifest.json summarising row counts and parse rates.

Usage:
    python scripts/build_qwk_inputs.py
    python scripts/build_qwk_inputs.py --input outputs/test_eval/predictions \\
                                       --output-dir data/qwk
"""
from __future__ import annotations

import argparse
import csv
import json
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parents[1]
DEFAULT_PREDICTIONS = REPO_ROOT / "outputs" / "test_eval" / "predictions"
DEFAULT_OUTPUT = REPO_ROOT / "data" / "qwk"


def decompose_score(score: int | None) -> tuple[int | None, int | None]:
    """Return (direction, confidence) or (None, None) if score is missing."""
    if score is None:
        return None, None
    s = int(score)
    if 1 <= s <= 5:
        return -1, s
    if 6 <= s <= 10:
        return 0, s - 5
    if 11 <= s <= 15:
        return 1, s - 10
    raise ValueError(f"score out of 1-15 range: {s}")


def read_predictions(root: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    for task_dir in sorted(root.iterdir()):
        if not task_dir.is_dir():
            continue
        for jsonl_path in sorted(task_dir.glob("*.jsonl")):
            with jsonl_path.open("r", encoding="utf-8") as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    rows.append(json.loads(line))
    return rows


def build_qwk_row(r: dict[str, Any]) -> dict[str, Any]:
    gd, gc = decompose_score(r["gold_score"])
    pd_, pc = decompose_score(r.get("pred_score"))
    return {
        "article_id":         r["article_id"],
        "task_model_slug":    r["task_model_slug"],
        "source_model_slug":  r["source_model_slug"],
        "prompt_idx":         r["prompt_idx"],
        "is_seed":            r.get("is_seed"),
        "is_best":            r.get("is_best"),
        "prompt_sha256":      r.get("prompt_sha256"),
        "parse_status":       r["parse_status"],
        "gold_score":         r["gold_score"],
        "pred_score":         r.get("pred_score"),
        "gold_direction":     gd,
        "gold_confidence":    gc,
        "pred_direction":     pd_,
        "pred_confidence":    pc,
    }


CSV_COLUMNS = [
    "article_id", "task_model_slug", "source_model_slug", "prompt_idx",
    "is_seed", "is_best", "parse_status",
    "gold_score", "pred_score",
    "gold_direction", "gold_confidence",
    "pred_direction", "pred_confidence",
]


def write_outputs(
    out_dir: Path,
    by_task: dict[str, list[dict[str, Any]]],
) -> list[dict[str, Any]]:
    out_dir.mkdir(parents=True, exist_ok=True)
    summary: list[dict[str, Any]] = []
    for task, qwk_rows in sorted(by_task.items()):
        task_dir = out_dir / task
        task_dir.mkdir(parents=True, exist_ok=True)
        jsonl_path = task_dir / f"{task}.jsonl"
        csv_path = task_dir / f"{task}.csv"

        with jsonl_path.open("w", encoding="utf-8") as f:
            for r in qwk_rows:
                f.write(json.dumps(r, ensure_ascii=False) + "\n")

        with csv_path.open("w", newline="", encoding="utf-8") as f:
            w = csv.DictWriter(f, fieldnames=CSV_COLUMNS, extrasaction="ignore")
            w.writeheader()
            for r in qwk_rows:
                w.writerow(r)

        n_ok = sum(1 for r in qwk_rows if r["parse_status"] == "ok")
        summary.append({
            "task_model_slug": task,
            "n_rows":          len(qwk_rows),
            "n_parsed":        n_ok,
            "parse_rate":      round(n_ok / len(qwk_rows), 4) if qwk_rows else 0.0,
            "jsonl":           str(jsonl_path.relative_to(REPO_ROOT)),
            "csv":             str(csv_path.relative_to(REPO_ROOT)),
        })
        print(
            f"[INFO] {task:<16} {len(qwk_rows):>5} rows  "
            f"{n_ok:>5} parsed  ({n_ok/len(qwk_rows):>5.1%})  "
            f"-> {task}/{jsonl_path.name}, {task}/{csv_path.name}"
        )
    return summary


def write_readme(out_dir: Path, summary: list[dict[str, Any]]) -> None:
    body = ["# QWK input datasets",
            "",
            "Generated by `scripts/build_qwk_inputs.py` from",
            "`outputs/test_eval/predictions/<task>/*.jsonl`.",
            "",
            "One subdirectory per task model, each containing two files:",
            "",
            "- `<task_model_slug>/<task_model_slug>.jsonl` — full schema, one row per "
            "(article, prompt).",
            "- `<task_model_slug>/<task_model_slug>.csv` — slim columns suitable for "
            "direct kappa input.",
            "",
            "## Schema (per row)",
            "",
            "| field | type | source |",
            "|---|---|---|",
            "| `article_id`        | string | persisted |",
            "| `task_model_slug`   | string | persisted |",
            "| `source_model_slug` | string | persisted |",
            "| `prompt_idx`        | int    | persisted |",
            "| `is_seed`, `is_best`| bool   | persisted |",
            "| `prompt_sha256`     | string | persisted (jsonl only) |",
            "| `parse_status`      | `ok` / `failed` | persisted |",
            "| `gold_score`        | int 1-15 | persisted |",
            "| `pred_score`        | int 1-15 or null | persisted |",
            "| `gold_direction`    | int -1 / 0 / +1 | derived |",
            "| `gold_confidence`   | int 1-5 | derived |",
            "| `pred_direction`    | int -1 / 0 / +1 or null | derived |",
            "| `pred_confidence`   | int 1-5 or null | derived |",
            "",
            "## Decomposition rule",
            "",
            "```",
            "score 1..5    -> direction = -1, confidence = score",
            "score 6..10   -> direction =  0, confidence = score - 5",
            "score 11..15  -> direction = +1, confidence = score - 10",
            "```",
            "",
            "## How to use",
            "",
            "```python",
            "import pandas as pd",
            "from sklearn.metrics import cohen_kappa_score",
            "",
            "df = pd.read_csv('data/qwk/claude_sonnet46/claude_sonnet46.csv')",
            "ok = df[df['parse_status'] == 'ok']",
            "",
            "qwk_dir = cohen_kappa_score(",
            "    ok['gold_direction'], ok['pred_direction'], weights='quadratic'",
            ")",
            "qwk_conf = cohen_kappa_score(",
            "    ok['gold_confidence'], ok['pred_confidence'], weights='quadratic'",
            ")",
            "```",
            "",
            "## Manifest",
            "",
            "| task_model_slug | rows | parsed | parse_rate | jsonl | csv |",
            "|---|---:|---:|---:|---|---|",
        ]
    for s in summary:
        body.append(
            f"| `{s['task_model_slug']}` | {s['n_rows']} | {s['n_parsed']} | "
            f"{s['parse_rate']:.2%} | `{s['jsonl']}` | `{s['csv']}` |"
        )
    (out_dir / "README.md").write_text("\n".join(body) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--input",
                        default=str(DEFAULT_PREDICTIONS),
                        help="root containing predictions/<task>/*.jsonl")
    parser.add_argument("--output-dir",
                        default=str(DEFAULT_OUTPUT),
                        help="where to write per-task QWK input files")
    args = parser.parse_args()

    src = Path(args.input)
    out = Path(args.output_dir)
    if not src.exists():
        sys.exit(f"[ERROR] predictions root not found: {src}")

    raw = read_predictions(src)
    print(f"[INFO] read {len(raw)} rows from {src}")

    by_task: dict[str, list[dict[str, Any]]] = defaultdict(list)
    for r in raw:
        by_task[r["task_model_slug"]].append(build_qwk_row(r))

    summary = write_outputs(out, by_task)
    write_readme(out, summary)
    (out / "manifest.json").write_text(json.dumps(summary, indent=2), encoding="utf-8")
    print(f"[INFO] manifest -> {out / 'manifest.json'}")
    print(f"[INFO] readme   -> {out / 'README.md'}")


if __name__ == "__main__":
    main()
